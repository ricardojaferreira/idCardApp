CREATE TABLE cards (
  id INTEGER PRIMARY KEY,
  cardname VARCHAR,
  name VARCHAR,
  job VARCHAR,
  company VARCHAR,
  email VARCHAR,
  description VARCHAR,
  photoPath VARCHAR,
  backgroundPath VARCHAR,
  backgroundSize VARCHAR,
  textColor VARCHAR
);

